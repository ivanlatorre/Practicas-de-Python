# AND
Resultado1 = True & True #Devolver True
Resultado2 = False & True #Devolver Falso
Resultado3 = True & False #Devolver False
Resultado4 = False & False # Devolver Falso
#OR
Resultado5 = True | True #Devolver True
Resultado6 = False | True #Devolver True
Resultado7 = True | False #Devolver True
Resultado8 = False | False #Devolver Falso
#NOT
Resultado9 = not True #Devolver Falso
Resultado10 = not False #Devolver True
print("----- AND -----")
print("Resultado1 AND // True  & True:  ",Resultado1)
print("Resultado2 AND // False & True:  ",Resultado2)
print("Resultado3 AND // True  & False: ",Resultado3)
print("Resultado4 AND // False & False: ",Resultado4)
print("----- OR -----")
print("Resultado5 OR // True  | True:  ",Resultado5)
print("Resultado6 OR // False | True:  ",Resultado6)
print("Resultado7 OR // True  | False: ",Resultado7)
print("Resultado8 OR // False | True:  ",Resultado8)
print("----- NOT -----")
print("Resultado9 NOT // not True:  ",Resultado9)
print("Resultado9 NOT // not False: ",Resultado10)





